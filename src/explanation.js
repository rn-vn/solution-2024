//SDGsビンゴの説明文//

SDGsビンゴはSDGsの目標を達成する行動をビンゴ形式で楽しく学べるゲームです

アプリを起動してホーム画面のビンゴカードを表示します

画面に表示される数字をタップして与えられたミッションをクリアしましょう

ミッションはSDGsの目標に関連した行動をすることやSDGsに関する知識を身につけることです

縦/横/斜めのいずれかにClearが3つ揃ったらビンゴです

ビンゴカードは1週間で更新されます


遊び方

アプリを起動します
ホーム画面のビンゴカードを表示します
画面に表示される数字をタップします
ミッションをクリアします
ビンゴカードの好きなところを1つClearにします

ミッション

SDGsの目標に関連した行動をすることやSDGsに関する知識を身につけることです

たとえば

* ペットボトルのゴミを捨てる
* エコバッグを利用する
* SDGsに関する記事を読む
* SDGsの目標について調べる

などのミッションが用意されています

*ビンゴカードは

ビンゴカードはSDGsの目標ごとに1から9までの数字が割り当てられています

ミッションをクリアすると割り当てられた数字がClearになります

縦/横/斜めのいずれかにClearが3つ揃ったらビンゴです

*ビンゴカードの更新

ビンゴカードは1週間で更新されます

新しいビンゴカードでは新しいミッションが用意されます

SDGsを学習する

SDGsに関連する記事を読みましょう

指定字以上の要約をするとビンゴカードの好きなところを1つClearにすることができます

SDGsに関する知識を身につけることでビンゴカードをよりクリアしやすくなります